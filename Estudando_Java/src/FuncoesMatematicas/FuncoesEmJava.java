public class FuncoesEmJava {
    public static void main(String[] args){
        //em java temos algumas funções que podem facilitar na hora de fazer algum calculo matematico
        //Math.sqrt() = para achar a raiz quadrada de algum numero
        //Math.pow() = para fazer um numero elevado a outro
        //Math.abs() = para achar o valor absoluto, ou seja, para ignorar a parte negativa caso ela exista

        double x = 3.0;
        double y = 3.0;
        double z = -3.0;

        System.out.println(Math.sqrt(x));
        System.out.println(Math.pow(x,y));
        System.out.println(Math.abs(z));
    }
}
